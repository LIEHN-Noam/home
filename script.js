$(document).ready(function (){
    $("#mini").hover(function(){
          $(this).fadeOut(200, function(){
            $(this).attr('src', 'img/miniloupe.png').fadeIn(200);
            });
        },function(){
          $(this).fadeOut(200, function(){
            $(this).attr('src', 'img/mini.png').fadeIn(200);
            });
        });
})
